        String inputPassword = new String(jPasswordField1.getPassword()).trim();
        
        if(inputUsername.equals(Login_And_SignUp.username) && inputPassword.equals(Login_And_SignUp.password)){
          JOptionPane.showMessageDialog(this,"Login successfull!", "welcome" ,JOptionPane.INFORMATION_MESSAGE);
        } else {
          JOptionPane.showMessageDialog(this,"Invalid Username or password.","Login Failed",JOptionPane.ERROR_MESSAGE);
        }
